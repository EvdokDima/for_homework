#include <iostream> 
#include <time.h>
using namespace std;

const int width = 32;
const int height = 14;

void print_field(char field[height][width]) {
	int i = 0;
	int j = 0;
	for (i; i < height; i++) {
		for (j; j < width; j++) {
			if (field[i][j] == '-') {
				cout << "\033[3;40;30m  \033[0m";
			}
			else if (field[i][j] == '@') {
				cout << "\033[3;44;30m  \033[0m";
			}
			else if (field[i][j] == '%') {
				cout << "\033[3;47;30m  \033[0m";
			}
			else if (field[i][j] == '#') {
				cout << "\033[3;42;30m  \033[0m";
			}
		}
		j = 0;
		cout << "\n";
	}
	cout << "\n";
}

int main() {
	char field[height][width] = { {"-------------------------------"},
								  {"############-------############"},
								  {"#@%%@@@@@%@#-------#@@@@@@@@@@#"},
		                          {"#@@@@%%%@%@#-------#@@@@@@@@@@#"},
							      {"#%%%@@@@@@@#-------#@@@@@@@@@@#"},
								  {"#@@@@@@@@@@#-------#@@@@@@@@@@#"},
								  {"#@@@@@@@@@@#-------#@@@@@@@@@@#"}, 
								  {"#@@@@%%@%@@#-------#@@@@@@@@@@#"},
								  {"#@@@@@@@@@@#-------#@@@@@@@@@@#"},
								  {"#%@%%%%@%@%#-------#@@@@@@@@@@#"},
								  {"#@@@@@@@@@@#-------#@@@@@@@@@@#"},
								  {"#@@@@@@@@@@#-------#@@@@@@@@@@#"},
							      {"############-------############"},
							      {"-------------------------------"} };

	print_field(field);
	return 0;
}